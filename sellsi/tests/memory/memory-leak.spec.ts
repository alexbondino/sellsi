test.describe('Memory Leak Detection - Buyer Flow', () => {
  test('should maintain stable memory after repeated navigation cycles', async () => {
    const runner = new BuyerFlowRunner({ headless: false });
    const memorySnapshots: MemorySnapshot[] = [];

    try {
      // Setup browser + CDP
      await runner.setup();

      // Login (detecta autom√°ticamente si ya hay sesi√≥n)
      await runner.login();

      // Navegar al marketplace
      await runner.navigateToMarketplace();

      // Snapshot inicial despu√©s de login
      memorySnapshots.push(await getMemoryUsage(runner.cdp, 'üìç Inicial (post-login)'));

      // Ciclos de navegaci√≥n
      for (let cycle = 1; cycle <= CONFIG.cycles.memory; cycle++) {
        await runner.runCycle(cycle);

        // Snapshot despu√©s de cada ciclo
        memorySnapshots.push(await getMemoryUsage(runner.cdp, `üìç Ciclo ${cycle} completado`));
      }

      // Reporte final
      const memoryGrowth = printMemoryReport(memorySnapshots);

      // Assertion: memoria no debe crecer m√°s del umbral
      expect(memoryGrowth).toBeLessThan(CONFIG.thresholds.memoryGrowthMB);

    } finally {
      await runner.close();
    }
  });
});
